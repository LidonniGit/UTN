    private void Cargar() {
        
        EspecialidadJpaController controlEspecialidad = new EspecialidadJpaController();
        List<Especialidad> listaSer = new ArrayList();
        listaSer = controlEspecialidad.findEspecialidadEntities();
        for (Especialidad especialidad : listaSer) {
            jCboxEspecialidad.addItem(especialidad);
        }
    }
    
    private void limpiar() {
        jTxtCodigo.setText("");
        jTxtTipo.setText("");
        jTxtDescripcion.setText("");
    }
