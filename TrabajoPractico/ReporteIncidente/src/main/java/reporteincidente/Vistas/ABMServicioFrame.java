    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        
        if ((!jTxtTipo.getText().isEmpty()) && (!jTxtDescripcion.getText().isEmpty())) {
          ServicioJpaController controlServicio = new ServicioJpaController();
            if (controlServicio.buscarPorTipo(jTxtTipo.getText())){
            
            Servicio servicioAux = new Servicio();
            servicioAux.setTipo(jTxtTipo.getText());
            servicioAux.setDescripcion(jTxtDescripcion.getText());
            servicioAux.setEspecialidad((Especialidad) jCboxEspecialidad.getSelectedItem());
                       controlServicio.create(servicioAux);
            limpiar();
            }else{
                JOptionPane.showMessageDialog(this, "Tipo ya existe");
            }
        }

    }//GEN-LAST:event_jBtnGuardarActionPerformed


